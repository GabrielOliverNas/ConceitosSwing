    private void btnCalcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcActionPerformed
        int numero = Integer.parseInt(txtNum.getValue().toString());
        
        int resto = numero % 2;
        lblResto.setText(Integer.toString(resto));
        
        double cubo = Math.pow(numero, 3);
        lblCubo.setText(Double.toString(cubo));
        
        double raiz = Math.sqrt(numero);
        lblRaizQuadrada.setText(String.format("%.2f",raiz));
        
        double raizC = Math.cbrt(numero);
        lblRaizC.setText(String.format("%.2f", raizC));
        
        int absouto = Math.abs(numero);
        lblRaizC.setText(Integer.toString(absouto));
        
        painelCalc.setVisible(true);
        
    }//GEN-LAST:event_btnCalcActionPerformed

